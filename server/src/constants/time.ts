// Considering that 1 equals one millisecond we get the following time constants:

const ONE_SECOND = 1000;

const ONE_MINUTE = 60 * ONE_SECOND;

const ONE_HOUR = 60 * ONE_MINUTE;

export const ONE_DAY = 24 * ONE_HOUR;

export const ONE_MONTH = 30 * ONE_DAY;

// Considering that 1 equals one second we get the following time constants:

export const ONE_MONTH_IN_SECONDS = ONE_MONTH / ONE_SECOND;
